def letterInWord(sketch, word, guess):
    i=0
    for letter in word:
        if guess == letter:
            sketch+=f" {guess} "
    
        else:
            sketch+=" __"
            i+=1

    return i

def isLetter():
    counter = 0
    letters = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
    while counter==0:
        guess = input("\nInput a valid alphabetical character: ").lower()
        for a in letters:
            if a==guess:
                counter+=1
    return guess

